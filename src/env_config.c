// 生成环境变量配置脚本
int generate_env_script(const char *home_dir, const char *output_path) {
    FILE *fp = fopen(output_path, "w");
    if (fp == NULL) {
        print_error("Failed to create environment script");
        return -1;
    }
    
    fprintf(fp, "#!/bin/bash\n");
    fprintf(fp, "# Environment configuration script generated by debpkg\n");
    fprintf(fp, "# Source this file to set up environment variables\n");
    fprintf(fp, "\n");
    fprintf(fp, "# Add executables to PATH\n");
    fprintf(fp, "export PATH=\"%s/.local/bin:$PATH\"\n", home_dir);
    fprintf(fp, "\n");
    fprintf(fp, "# Add libraries to LD_LIBRARY_PATH\n");
    fprintf(fp, "export LD_LIBRARY_PATH=\"%s/.local/lib:$LD_LIBRARY_PATH\"\n", home_dir);
    fprintf(fp, "\n");
    fprintf(fp, "# Add headers to C_INCLUDE_PATH (optional, for development)\n");
    fprintf(fp, "export C_INCLUDE_PATH=\"%s/.local/include:$C_INCLUDE_PATH\"\n", home_dir);
    fprintf(fp, "\n");
    fprintf(fp, "# Add man pages to MANPATH (optional)\n");
    fprintf(fp, "export MANPATH=\"%s/.local/share/man:$MANPATH\"\n", home_dir);
    fprintf(fp, "\n");
    fprintf(fp, "echo \"Environment variables configured successfully!\"\n");
    
    fclose(fp);
    
    // 设置可执行权限
    chmod(output_path, 0755);
    
    print_success("Environment script created: ");
    printf("%s\n", output_path);
    
    return 0;
}

// 检查并提示环境变量配置
int check_and_prompt_env_config(const char *home_dir, const char *pkg_name) {
    char bin_path[512];
    char lib_path[512];
    char config_script[512];
    int needs_config = 0;
    
    snprintf(bin_path, sizeof(bin_path), "%s/.local/bin", home_dir);
    snprintf(lib_path, sizeof(lib_path), "%s/.local/lib", home_dir);
    snprintf(config_script, sizeof(config_script), "%s/.local/debpkg_env.sh", home_dir);
    
    // 检测环境变量问题
    int issues = detect_env_issues(home_dir);
    
    if (issues > 0) {
        printf("\n");
        print_warning("Environment variables may need configuration!");
        printf(COLOR_YELLOW "[INFO] " COLOR_RESET "%d environment variable(s) need attention.\n", issues);
        printf("\n");
        
        // 检查是否已生成过配置脚本
        if (!file_exists(config_script)) {
            printf("Do you want to generate an environment configuration script? (y/n): ");
            
            char choice;
            if (scanf(" %c", &choice) != 1) {
                print_error("Invalid input");
                return -1;
            }
            
            if (choice == 'y' || choice == 'Y') {
                if (generate_env_script(home_dir, config_script) == 0) {
                    printf("\n");
                    print_info("To activate the environment variables, run:");
                    printf(COLOR_GREEN "  source %s\n" COLOR_RESET, config_script);
                    printf("\n");
                    printf("Or add the following lines to your ~/.bashrc or ~/.zshrc:\n");
                    printf(COLOR_YELLOW "  source $HOME/.local/debpkg_env.sh\n" COLOR_RESET);
                    printf("\n");
                }
            }
        } else {
            print_info("Environment script already exists:");
            printf(COLOR_GREEN "  %s\n" COLOR_RESET, config_script);
            printf("\n");
            printf("To activate, run:\n");
            printf(COLOR_GREEN "  source %s\n" COLOR_RESET, config_script);
            printf("\n");
        }
        
        // 显示详细配置指南
        printf("Would you like to see detailed environment configuration guide? (y/n): ");
        
        char show_guide;
        if (scanf(" %c", &show_guide) != 1) {
            return -1;
        }
        
        if (show_guide == 'y' || show_guide == 'Y') {
            show_env_config_guide(home_dir);
        }
    } else {
        print_success("All environment variables are properly configured!");
    }
    
    return 0;
}

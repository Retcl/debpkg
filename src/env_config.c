// 检查环境变量是否已配置
static int check_env_var(const char *var_name, const char *search_path) {
    const char *env_value = getenv(var_name);
    
    if (env_value == NULL) {
        return 0;  // 环境变量未设置
    }
    
    // 检查路径是否在环境变量中
    if (strstr(env_value, search_path) != NULL) {
        return 1;  // 已配置
    }
    
    return 0;  // 未配置
}

// 检测是否需要配置环境变量
int detect_env_issues(const char *home_dir) {
    char bin_path[512];
    char lib_path[512];
    char include_path[512];
    char man_path[512];
    int issues = 0;
    
    snprintf(bin_path, sizeof(bin_path), "%s/.local/bin", home_dir);
    snprintf(lib_path, sizeof(lib_path), "%s/.local/lib", home_dir);
    snprintf(include_path, sizeof(include_path), "%s/.local/include", home_dir);
    snprintf(man_path, sizeof(man_path), "%s/.local/share/man", home_dir);
    
    // 检查 PATH
    if (!check_env_var("PATH", bin_path)) {
        issues++;
    }
    
    // 检查 LD_LIBRARY_PATH
    if (!check_env_var("LD_LIBRARY_PATH", lib_path)) {
        issues++;
    }
    
    // 检查 C_INCLUDE_PATH (可选)
    if (is_directory(include_path)) {
        if (!check_env_var("C_INCLUDE_PATH", include_path)) {
            issues++;
        }
    }
    
    // 检查 MANPATH (可选)
    if (is_directory(man_path)) {
        if (!check_env_var("MANPATH", man_path)) {
            issues++;
        }
    }
    
    return issues;
}

